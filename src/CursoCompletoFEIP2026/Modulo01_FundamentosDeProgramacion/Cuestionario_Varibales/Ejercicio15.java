package CursoCompletoFEIP2026.Modulo01_FundamentosDeProgramacion.Cuestionario_Varibales;

public class Ejercicio15 {
    public static void main(String[] args) {
        /*
         * Pregunta 15:
         * ¿Cuál es la forma correcta de declarar un literal long en Java?
         * 1. long numero = 123423425;
         * 2. long numero = 1231312412345l;
         * 3. long numero = 1231312412345l;
         * 4. 2 y 3 son correctas.
         * 5. long numero = L1231242532523;
         *
         * a. La 4 ✅
         * b. La 1
         * c. Solo la 2 es la correcta
         * d. La 5
         *
         */

        // Ejemplo
        // Respuesta correcta: 12134323551L y 1231312412345l
        long numero1 = 12134323551L;
        long numero2 = 1231312412345l;
        System.out.println("Las respuestas correctas son: " + numero1 + numero2);

    }
}
