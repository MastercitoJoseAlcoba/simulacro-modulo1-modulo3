public class Ejercicio16 {
    public static void main(String[] args) {
        /*
         * Pregunta 16:
         * ¿Cuál será el resultado de la siguiente operación?
         * double a = 50.0;
         * double b = 3.0;
         * float result = (float) (a / b);
         *
         * a. 16.66666667
         * b. 17.0
         * c. 16.66666603 ✅
         * d. 16.67
         *
         */

        double a = 50.0;
        double b = 3.0;
        float result = (float) (a / b);
        System.out.println(result); // Imprime: 16.666666
    }
}
