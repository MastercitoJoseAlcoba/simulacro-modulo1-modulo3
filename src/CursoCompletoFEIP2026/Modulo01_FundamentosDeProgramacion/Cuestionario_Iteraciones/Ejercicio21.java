package CursoCompletoFEIP2026.Modulo01_FundamentosDeProgramacion.Cuestionario_Iteraciones;

public class Ejercicio21 {
    public static void main(String[] args) {
        for (int i=5; i>0; i--) {
            System.out.println(i + "-");
            System.out.println("#");
            // Imprime: 5-4-3-2-1-#
        }
    }
}
